def parse_iters_newformat(text):
    """
    Parse the new-format logs into a list of dictionaries.
    Header:
      iter | pri res | dua res |   gap   |   obj   |  scale  | time (s)
    Field mapping:
      - ite      <- iter
      - POBJ     <- obj
      - DOBJ     <- obj  (no explicit dual; reuse obj to keep plotting layout)
      - GAP_raw  <- gap
      - PRES_raw <- pri res
      - DRES_raw <- dua res
      - SCALE    <- scale
      - TIME     <- time (s)
    """
    rows = []
    in_table = False
    header_re = re.compile(
        r"^\s*iter\s*\|\s*pri\s+res\s*\|\s*dua\s+res\s*\|\s*gap\s*\|\s*obj\s*\|\s*scale\s*\|\s*time\s*\(s\)\s*$",
        re.IGNORECASE
    )
    sep_re = re.compile(r"^\s*[-_]{3,}\s*$")

    # Data line example:
    # "   250| 1.32e+05  2.99e-02  1.46e+06 -2.50e+08  1.00e-06  7.99e-01 "
    line_re = re.compile(
        r"^\s*(?P<iter>-?\d+)\s*\|\s*"
        r"(?P<pri_res>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<dua_res>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<gap>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<obj>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<scale>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<time>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s*$"
    )

    for raw in text.splitlines():
        line = raw.rstrip("\n")
        if not in_table:
            if header_re.search(line):
                in_table = True
            continue
        # After entering the table, skip separator lines
        if sep_re.search(line):
            continue
        m = line_re.match(line)
        if not m:
            # Skip non-data lines
            continue
        g = m.groupdict()
        try:
            ite = int(g["iter"])
            pri_res = float(g["pri_res"])
            dua_res = float(g["dua_res"])
            gap = float(g["gap"])
            obj = float(g["obj"])
            scale = float(g["scale"])
            t = float(g["time"])
        except ValueError:
            continue

        rows.append({
            "ite": ite,
            "POBJ": obj,
            "DOBJ": obj,          # Placeholder (no dual value)
            "PFEAS": float("nan"),
            "DFEAS": float("nan"),
            "GFEAS": float("nan"),
            "PRSTATUS": float("nan"),
            "MU": float("nan"),
            "TIME": t,
            "GAP_raw": gap,
            "PRES_raw": pri_res,
            "DRES_raw": dua_res,
            "KT_raw": float("nan"),
            "SCALE": scale,
            "STEP_raw": float("nan"),
        })

    rows.sort(key=lambda r: r["ite"])
    return rows

