def parse_iters_strict(text):
    """
    Parse CVXPY log blocks into a list of dicts.
    Column order: ITE PFEAS DFEAS GFEAS PRSTATUS POBJ DOBJ MU TIME
    """
    rows = []
    line_re = re.compile(
        r"^\(CVXPY\).*?:\s*"
        r"(?P<ite>-?\d+)\s+"
        r"(?P<PFEAS>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<DFEAS>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<GFEAS>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<PRSTATUS>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<POBJ>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<DOBJ>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<MU>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s+"
        r"(?P<TIME>[+-]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][+-]?\d+)?)\s*$"
    )
    for line in text.strip().splitlines():
        m = line_re.match(line.rstrip())
        if not m:
            continue
        g = m.groupdict()
        try:
            rows.append({
                "ite": int(g["ite"]),
                "PFEAS": float(g["PFEAS"]),
                "DFEAS": float(g["DFEAS"]),
                "GFEAS": float(g["GFEAS"]),
                "PRSTATUS": float(g["PRSTATUS"]),
                "POBJ": float(g["POBJ"]),
                "DOBJ": float(g["DOBJ"]),
                "MU": float(g["MU"]),
                "TIME": float(g["TIME"]),
            })
        except ValueError:
            continue
    rows.sort(key=lambda r: r["ite"])
    return rows

